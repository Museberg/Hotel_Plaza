import java.util.*;
import java.io.*;

public class Staff{
   String title;
   String firstName;
   String lastName;
   int phoneNumber;
   double salary;
   
   public Staff(String title, String firstName, String lastName, int phoneNumber, double salary){
      this.title = title;
      this.firstName = firstName;
      this.lastName = lastName;
      this.phoneNumber = phoneNumber;
      this.salary = salary;
   }
   
}